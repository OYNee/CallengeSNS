import { gql } from "apollo-boost";

export const CHALLENGE = gql`
  query challenge($id: String!) {
    seeChallenge(id: $id) {
      id
      location
      caption
      user {
        id
        avatar
        username
      }
      files {
        id
        url
      }
      likeCount
      isLiked
      comments {
        id
        text
        user {
          id
          username
        }
      }
      createdAt
    }
  }
`;